// Mostrar pares e impares del 1 al 10
function calcularParEImpar() {
  for (let i = 0; i <= 10; i++) {
    if (i % 2 === 0) {
      console.log(`el número ${i} es par.`);
    } else {
      console.log(`el número ${i} es impar.`);
    }
  }
}
calcularParEImpar();

